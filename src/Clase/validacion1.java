    public void comprobarEspacioBlanco(String cadena){
       int cantidadDeEspacios = 0;
        // Recorremos la cadena:
        for (int i = 0; i < cadena.length(); i++) {
            // Si el carÃ¡cter en [i] es un espacio (' ') aumentamos el contador 
            if (cadena.charAt(i) == ' ') cantidadDeEspacios++;
        }
        // Finalmente lo imprimimos
        JOptionPane.showMessageDialog(this,"La cantidad de espacios es: " + cantidadDeEspacios);
    }


    
    
